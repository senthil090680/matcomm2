<b>Special cultural nuances: </b><br />
Thali kettu kalyanam (mock marriage ceremony)
A thali kettu kalyanam or a mock marriage ceremony was prevalent among some rich Ezhavas (or Pramanis ). A thaali (a gold necklace tied around the bride's neck) tying rite took place before the onset of puberty. During this ceremony, the girl was forced to marry a man (strictly from the same community unlike other castes which followed this custom) whose horoscopes matched.<br /><br />
Because that man was simply her "mock" husband, he could leave her after the completion of the ritual. Sree Narayana Guru opposed this strongly and took the initiative to simplify marriage customs and celebrations<br />



