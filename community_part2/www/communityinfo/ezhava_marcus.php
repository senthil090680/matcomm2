                We are the largest Hindu community in Kerala and also one of the most progressive communities of the state. We are said to be descendants of Villavar the founders of the ancient Dravidian Chera Dynasty. We are also known as Thiyyas and Billavas.
Folklore and written records indicate that our community was also a martial class.
The origin of the word Ezhava is often disputed. The most plausible explanation is that it means "people from Ezham (Sri Lanka)". Thiyya seems to have originated from "Dweepan" which means a person living in an island (obvious reference to Sri Lanka).<br /><br />
Ezhava folk songs, the Vadakkan Pattukal, composed about 400 hundred years ago, described the military exploits of Ezhava heroes. Our ancestors served in the armed forces of all the important kings of the region like the Zamorins of Calicut and the Kings of Travancore and Cochin. <br /><br />
Although, traditionally agriculturists and toddy farmers, many Ezhavas were notable as Ayurvedic physicians, warriors and traders.<br /><br />
Sree Narayana Guru, the greatest social reformer Kerala has ever seen, is widely accepted as our spiritual, social and intellectual mentor and guiding spirit. He is credited with giving a unique identity to our community. The SNDP was formed to propagate and promote the moral teachings and Dharma of Shree Narayana Guru. <br /><br />
Today, Ezhavas make up a major community in Kerala, comprising of about 25 per cent of its population with a strong presence in politics, business, education and trade.<br /><br />
