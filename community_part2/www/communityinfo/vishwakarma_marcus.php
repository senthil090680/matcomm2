            Vishwakarma or Vishvabrahmin is the name given to a community comprising of goldsmiths, blacksmiths, copper-smiths, carpenters and sculptors. The community also has territorial subdivisions. Like other Brahmin communities the vishwabrahmins also have elaborate pre- marriage and post-marriage rituals.<br /><br />
            The Muhuratam: This is the first of the pre-marriage rituals. During this time, the auspicious date and time are fixed or the marriage.<br /><br />
            Pendlikoothuru: During this ceremony which is performed in the individual homes of the couple. Both the girl and the boy have a bath with turmeric and oil and wear new clothes.<br /><br />
            Mangala Snaanam, Gauri Pooja and Ganesh pooja: At dawn on the wedding day the both the bride and the bridegroom have a bath in their respective houses before proceeding for the marriage rituals. After the bath the girl worships goddess Durga. While the bride is performing the Gauri Pooja the groom performs Ganesh Pooja.<br /><br />
            <b>Marriage Rituals</b><br />
            Kanyadaan: The bride is brought to the wedding mandap in a basket. During the marriage ceremony the couple does not see each other. After the marriage ceremony is over the parents of the bride wash the groom's feet.<br /><br />
            Tying of the mangalsutra: The groom ties the mangalsutra around the girls neck.<br /><br />
            Kanya Daan Akshata: After the tying of the mangalsutra the couple exchanges garlands. The people who are attending the ceremony shower flower petals and rice on the couple.<br /><br />
            Saptapadi: The bride and the bride groom circle the fire seven times during this ceremony.<br />
