<div align="left" style="text-align: justify;" class="clr">The name of our community is derived from the word 'Ka Meendar', meaning 'Kanakathai azhiththu vilai nilangalai uruvakkiyavarkal' (one who converted forests into arable land). We are noted for our modern outlook and upward social mobility. Most Goundar families live in Tamil Nadu and Karnataka. Our community name is equivalent to Gowd, Goud and Gowda in Karnataka. Kongu Vellalar, Vanniar, Vettuvar and Gollars have also been included in our community.<br /><br /></div>
