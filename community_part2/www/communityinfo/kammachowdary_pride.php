                <img src="http://img.kammanaidumatrimony.com/images/Kammachowdary_pride.jpg" style="padding-right:12px;float:left;"/>Not many know that the macho hunk of Telugu films has exemplary academic achievements. Nagarjuna was born on August 29, 1959 in Chennai but did his schooling in Hyderabad. He returned to Chennai to pursue Mechanical engineering in College of Engineering, Guindy (Anna university, Chennai). He obtained an M.S Degree in Mechanical Engineering from University of Michigan. He is fluent in Hindi, Tamil, Telugu and English. <br /><br />
                Nagarjuna's debut movie Vikram was released on 24 May 1986. He hit the headlines in 1989 when he attained critical and commercial success with Geetanjali, a love story directed by Maniratnam. <br /><br />
                Geetanjali became a blockbuster hit when it was dubbed into Tamil as well. In the same year, Nagarjuna starred in the trendsetter blockbuster hit, Shiva, directed by Ram Gopal Varma.  It becomes one of the all time hits of the Telugu film industry. Shiva was dubbed into Tamil as 'Udhayam' and it was a run away hit Tamil too. <br /><br />
                Nagarjuna made his Bollywood debut with the Hindi remake of Udhayam, which too turned out to be a huge success all over India.  It was followed by a string of blockbusters like Varasudu, Gharana Bullodu, and Allari Alludu, Criminal and Sisindri.  <br /><br />
                In 1994, he acted in a comedy film called Hello Brother, another very successful venture.  It was later remade into Judwaa, a Hindi film starring Salman Khan. <br /><br />
                Hello Brother became a super hit in Tamil too. In 1996, Nagarjuna produced, Ninne Pelladutha, which was directed by Krishna Vamsi and co-starred the National Award winning Hindi actress, Tabu. It went on to become the biggest hit of that year.<br /><br />
                Nagarjuna portrayed Annamacharya, the 15th century Telugu singer and poet, in Annamayya. Like Shiva and Geethanjali, Annamayya. Nagarjuna bagged the National Award. <br /><br />
                His other films that year, Ninne Premistha and Aazaad too met with commercial success. As did the romantic comedies, Santhosham and Manmadhudu, both of which were blockbusters.<br /><br />
                In 2004, Nagarjuna's Nennunnanu and Mass became box office winners. Mass, which was produced by Nagarjuna, became one of the highest grossing films in his career. <br /><br />
                Two years later, Nagarjuna teamed up with Annamayya director, K.Raghavendra Rao, for Sri Ramadasu, his second period film.<br /><br />
                Nagarjuna received the Nandi award for best actor that year. Sri Ramadasu, much like his earlier Annamayya, was a huge hit for Nagarjuna. His most recent films, "Don" and "King" also clicked at the box office.<br />
