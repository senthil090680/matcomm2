            <img src="http://img.kshatriyamatrimony.com/images/Kshatriya_pride.jpg" style="padding-right:12px;float:left;"/><b>Shooting star</b><br />
            Rajyavardhan Singh Rathore created history at the 2004 Athens Games by becoming the first to win an individual Olympic silver medal for India. The silver came in the Men's Double Trap event. <br /><br />
            Rathore was born on January 29, 1970 in Jaisalmer, Rajasthan, in an army background. His father, Laxman Singh Rathore, is a colonel. in the Indian Army and his mother, Manju Rathore, a teacher. Rathore is called 'Chilly' at home. Right from childhood, he had a passion for shooting and cricket. And he nearly got selected to the Madhya Pradesh Ranji team when he was in class 10!<br /><br />
            However, given his age, his mother discouraged him from joining the Ranji team as she wanted him to complete his school education. He then took the National Defense Academy test and joined the army.<br /><br />
            During Rathore's term as an army major, he was conferred with the 'Sword of Honor'. Interestingly, it was only in 1998 that Rathore seriously took up shooting and the rest is history.<br /><br />
            He bagged the bronze at the World Championship in Cyprus and got a chance to compete in the Olympics in 2004. For the event, he took training from Olympic gold medalist Russel Mark of Australia in Europe. <br /><br />
            Rathore won the gold in the individual and team events in the men's double trap at the 2005 Asian Clay Shooting Championships at Bangkok. With this, he also completed a hat trick at the annual events, as he was the champion in 2003 and 2004 as well. <br /><br />
            In the 2005 Commonwealth Shooting Championships in Melbourne, Rathore was the member of the winning Indian team in the double trap event.
Rathore was conferred with the Rajiv Gandhi Khel Ratna Award, the highest sporting honor in India, for 2004.
<br /><br />
