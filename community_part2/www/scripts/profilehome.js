var TabNxtVar=0;

function sitetab(dname, hpscount) {

	for(var i=1; i<=hpscount; i++)
		{
			var divid = "Ldiv"+i;
			var tdivid = "L"+i;

			document.getElementById(divid).className="disnon fleft";
            document.getElementById(tdivid).className="clr1";
		}

		for(var i=1; i<=hpscount; i++)
		{
			var divid1 = "Ldiv"+i;
			var tdivid = "L"+i;

			if(divid1==dname && TabNxtVar==0)
			{
				document.getElementById(dname).className = "disblk fleft";
                document.getElementById(tdivid).className = "clr";
			}
		}
	}
